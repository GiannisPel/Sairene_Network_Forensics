#Split frames into line lists
frames_lines = [f.splitlines() for f in FRAMES]
max_h = max(len(fl) for fl in frames_lines)

#Pad all frames to same height
for fl in frames_lines:
    fl += [""] * (max_h - len(fl))

def move_up(n: int):
    #Move cursor up n lines and to column 1
    sys.stdout.write(f"\033[{n}A\033[0G")

def draw_frame(frame_lines):
    sys.stdout.write("\033[J")  #clear from cursor to end of screen
    sys.stdout.write("\n".join(frame_lines) + "\n")
    sys.stdout.flush()


# Speed / randomness
frame_delay = 0.12
min_idle = 5
max_idle = 15

def animate_once(frame_delay: float = 0.12):
    sys.stdout.write("\033[?7l")  # disable wrap
    sys.stdout.flush()

    try:
        #Reserve the space using frame 0
        draw_frame(frames_lines[0])
        move_up(max_h)

        #Forward: 1..end
        for fl in frames_lines[1:]:
            draw_frame(fl)
            move_up(max_h)
            time.sleep(frame_delay)

        #Backward: end-1..0
        for fl in reversed(frames_lines[:-1]):
            draw_frame(fl)
            move_up(max_h)
            time.sleep(frame_delay)

    finally:
        #Move cursor below the animation area and restore wrap
        sys.stdout.write(f"\033[{max_h}B")
        sys.stdout.write("\033[?7h")
        sys.stdout.write("\n")
        sys.stdout.flush()